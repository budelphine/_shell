#include "libft.h"

/*
** Что делает функция:		Проверяет принадлежность символа к ASCII.
**
** Возращаемое значение:	1 - В случае принадлежности;
**							0 - В обратном.
**
** Особенности:				Нет.
*/

int	ft_isascii(int c)
{
	return (c >= 0 && c <= 127);
}
